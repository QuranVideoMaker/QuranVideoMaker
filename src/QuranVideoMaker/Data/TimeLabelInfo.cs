namespace QuranVideoMaker.Data
{
    /// <summary>
    /// Represents information about a time label.
    /// </summary>
    public class TimeLabelInfo
    {
        /// <summary>
        /// Gets or sets the number associated with the time label.
        /// </summary>
        public int Number { get; set; }

        /// <summary>
        /// Gets or sets the X coordinate of the time label.
        /// </summary>
        public double X { get; set; }
    }
}
